package com.handheldgroup.nx6rtkhelper;

public class UbxCommands {

    public static byte[] enableGSTMessages() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x01, 0x03, 0x00, (byte) 0xF0, 0x07, 0x01, 0x02, 0x1E};
    }

    public static byte[] disableGSTMessages() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x01, 0x03, 0x00, (byte) 0xF0, 0x07, 0x00, 0x01, 0x1D};
    }

    public static byte[] enableGNSMessages() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x01, 0x03, 0x00, (byte) 0xF0, 0x0D, 0x01, 0x08, 0x2A};
    }

    public static byte[] disableGNSMessages() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x01, 0x03, 0x00, (byte) 0xF0, 0x0D, 0x00, 0x07, 0x29};
    }

    public static byte[] getRtkResetCommand() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, (byte) 0xD0,
                0x08, 0x00, 0x00, 0x00, (byte) 0xC2, 0x01, 0x00, 0x23, 0x00, 0x23, 0x00,
                0x00, 0x00, 0x00, 0x00, (byte) 0xFC, 0x1E};
    }

    public static byte[] setBaudRate115200() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, (byte) 0xD0,
                0x08, 0x00, 0x00, 0x00, (byte) 0xC2, 0x01, 0x00, 0x23, 0x00, 0x03, 0x00,
                0x00, 0x00, 0x00, 0x00, (byte) 0xDC, 0x5E};
    }

    public static byte[] setBaudRate38400() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, (byte) 0xD0,
                0x08, 0x00, 0x00, 0x00, (byte) 0x96, 0x00, 0x00, 0x07, 0x00, 0x03, 0x00,
                0x00, 0x00, 0x00, 0x00, (byte) 0x93, (byte) 0x90};
    }

    public static byte[] setBaudrate9600() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, (byte) 0xD0,
                0x08, 0x00, 0x00, (byte) 0x80, 0x25, 0x00, 0x00, 0x23, 0x00, 0x03, 0x00,
                0x00, 0x00, 0x00, 0x00, (byte) 0xBE, (byte) 0x95};
    }

    public static byte[] saveUbloxConfig() {
        return new byte[]{(byte) 0xB5, 0x62, 0x06, 0x09, 0x0D, 0x00, 0x00, 0x00, 0x00, 0x00, (byte) 0xFF,
                (byte) 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03, 0x1D, (byte) 0xAB};
    }
}
